package basic;

public class UnaryOperator {

	public static void main(String[] args) {
		int a = 20;
		System.out.println(a);
		
		System.out.println("Post Increament:"+ a++);
		System.out.println("After Post Increament:"+a);
		
		System.out.println("Pre Increament: "+ ++a);
		System.out.println("After Pre Increament: "+a);
		 
		System.out.println("Post Decreament: "+ a--);
		System.out.println("After Post Decreament:"+a);
		
		System.out.println("Pre Decreament: "+ --a);
		System.out.println("After Pre Decreament:"+a);

	}

}
